void remove(std::vector<int> &v)
{
    auto end = v.end();
    for (auto it = v.begin(); it != end; ++it)
    {
        end = std::remove(it + 1, end, *it);
    }

    v.erase(end, v.end());
}
void solve()
{
    ll n;
    cin >> n;
    int a[n], b[n];
    for (int i = 1; i <= n; i++)
        cin >> a[i];
    for (int i = 1; i <= n; i++)
        cin >> b[i];
    int cnt;
    bool check = false;
    for (int i = 1; i <= n; i++)
    {
        if (a[i] < b[i])
        {
            cout << "NO" << endl;
            return;
        }
        else
        {
            if (!check)
            {
                cnt = a[i] - b[i];
                check = true;
            }

            else
            {
                if (cnt < a[i] - b[i])
                {
                    cout << "NO" << endl;
                    return;
                }
            }
        }
    }
    cout << "YES" << endl;
}

